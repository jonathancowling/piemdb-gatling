const { checkReview } = require('./checkReview');

describe('/checkingFunctions/checkReview.js', () => {
  it('should return true for a valid review and name', () => {
    // valid name and 500 char review
    const testReview = {
      name: 'Ali Toyn',
      'review-text': '012345678901234567890123456789012345678901234567890123456789012345678901234567890123456789'
        + '012345678901234567890123456789012345678901234567890123456789012345678901234567890123456789'
        + '012345678901234567890123456789012345678901234567890123456789012345678901234567890123456789'
        + '012345678901234567890123456789012345678901234567890123456789012345678901234567890123456789'
        + '012345678901234567890123456789012345678901234567890123456789012345678901234567890123456789',
    };
    const output = checkReview(testReview);

    expect(output).toBe(true);
  });

  it('should return false for an invalid review', () => {
    // valid name and 600 char review
    const testReview = {
      name: 'Ali Toyn',
      'review-text': '012345678901234567890123456789012345678901234567890123456789012345678901234567890123456789'
        + '012345678901234567890123456789012345678901234567890123456789012345678901234567890123456789'
        + '012345678901234567890123456789012345678901234567890123456789012345678901234567890123456789'
        + '012345678901234567890123456789012345678901234567890123456789012345678901234567890123456789'
        + '012345678901234567890123456789012345678901234567890123456789012345678901234567890123456789'
        + '012345678901234567890123456789012345678901234567890123456789012345678901234567890123456789',
    };
    const output = checkReview(testReview);

    expect(output).toBe(false);
  });

  it('should return false for an invalid name', () => {
    // valid review and 30 char name
    const testReview = {
      name: '012345678901234567890123456789',
      'review-text': 'nice pie',
    };
    const output = checkReview(testReview);

    expect(output).toBe(false);
  });
});
